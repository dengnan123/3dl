/**
 * 通过 code 和 type 获取天气图标路径
 * @param {string | number} code 天气图标 code
 * @param {string} type 天气图标类型 实心 = solid， 空心 = linear
 * @returns {string} 天气图标路径
 */
export function getWeatherNameByCode(code, type = 'solid') {
  const name = weatherHash[code];
  const src = `https://3dl.dfocus.top/api/static/weather/${type}/${name}`;
  return src;
}

